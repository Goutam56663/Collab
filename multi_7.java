class multi_7
{
    void main (int i, int j)
    {
        if(i%7==0 )
        {
            System.out.println(i+ "is multiple by 7.");
        }
        else
        {
            System.out.println(i+ "is not multiple by 7.");
        }
        
        if(j%7==0 )
        {
            System.out.println(j+ "is multiple by 7.");
        }
        else
        {
            System.out.println(j+ "is not multiple by 7.");
        }
    }
}